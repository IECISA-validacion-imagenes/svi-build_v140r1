package mx.com.teclo.svi.negocio.vo.validacion;

public class ValidacionDatosVO {

	private Long totalValidadas;
	private Long totalRegistros;
	private Long totalRestantes;
	private Long idArchivo;
	private Boolean archivoValidado;
	private Long totalRegistrosAsignados;
	
	
	public Long getTotalValidadas() {
		return totalValidadas;
	}
	public void setTotalValidadas(Long totalValidadas) {
		this.totalValidadas = totalValidadas;
	}
	public Long getTotalRegistros() {
		return totalRegistros;
	}
	public void setTotalRegistros(Long totalRegistros) {
		this.totalRegistros = totalRegistros;
	}
	public Long getTotalRestantes() {
		return totalRestantes;
	}
	public void setTotalRestantes(Long totalRestantes) {
		this.totalRestantes = totalRestantes;
	}
	public Long getIdArchivo() {
		return idArchivo;
	}
	public void setIdArchivo(Long idArchivo) {
		this.idArchivo = idArchivo;
	}
	public Boolean getArchivoValidado() {
		return archivoValidado;
	}
	public void setArchivoValidado(Boolean archivoValidado) {
		this.archivoValidado = archivoValidado;
	}
	public Long getTotalRegistrosAsignados() {
		return totalRegistrosAsignados;
	}
	public void setTotalRegistrosAsignados(Long totalRegistrosAsignados) {
		this.totalRegistrosAsignados = totalRegistrosAsignados;
	}
	
	
	
	
	
	
	
	
}
