package mx.com.teclo.svi.negocio.vo.reporte;

public class rptInfraccionesMensuales {
	
	private String deposito;
	private String anio;
	private int mes;
	
	private String medioIngreso;
	private String cantidad;
	/**
	 * @return the deposito
	 */
	public String getDeposito() {
		return deposito;
	}
	/**
	 * @param deposito the deposito to set
	 */
	public void setDeposito(String deposito) {
		this.deposito = deposito;
	}
	/**
	 * @return the anio
	 */
	public String getAnio() {
		return anio;
	}
	/**
	 * @param anio the anio to set
	 */
	public void setAnio(String anio) {
		this.anio = anio;
	}
	/**
	 * @return the mes
	 */
	public int getMes() {
		return mes;
	}
	/**
	 * @param mes the mes to set
	 */
	public void setMes(int mes) {
		this.mes = mes;
	}
	/**
	 * @return the medioIngreso
	 */
	public String getMedioIngreso() {
		return medioIngreso;
	}
	/**
	 * @param medioIngreso the medioIngreso to set
	 */
	public void setMedioIngreso(String medioIngreso) {
		this.medioIngreso = medioIngreso;
	}
	/**
	 * @return the cantidad
	 */
	public String getCantidad() {
		return cantidad;
	}
	/**
	 * @param cantidad the cantidad to set
	 */
	public void setCantidad(String cantidad) {
		this.cantidad = cantidad;
	}
	
	
	

}
