package mx.com.teclo.svi.negocio.vo.catalogo;

/*
 * catalogo para nuevo formato de busque da dpeerfil */

public class CatalogoMarcaSubVO {
	private String idPerfil;
	private String idMarca;
	private String idSubmarca;
	private String marcaSubPerfil;
	private String urlImagen; 
	private String txtPerfil;
	
	
	/**
	 * @return the idPerfil
	 */
	public String getIdPerfil() {
		return idPerfil;
	}
	/**
	 * @param idPerfil the idPerfil to set
	 */
	public void setIdPerfil(String idPerfil) {
		this.idPerfil = idPerfil;
	}
	/**
	 * @return the idMarca
	 */
	public String getIdMarca() {
		return idMarca;
	}
	/**
	 * @param idMarca the idMarca to set
	 */
	public void setIdMarca(String idMarca) {
		this.idMarca = idMarca;
	}
	/**
	 * @return the idSubmarca
	 */
	public String getIdSubmarca() {
		return idSubmarca;
	}
	/**
	 * @param idSubmarca the idSubmarca to set
	 */
	public void setIdSubmarca(String idSubmarca) {
		this.idSubmarca = idSubmarca;
	}
	/**
	 * @return the marcaSubPerfil
	 */
	public String getMarcaSubPerfil() {
		return marcaSubPerfil;
	}
	/**
	 * @param marcaSubPerfil the marcaSubPerfil to set
	 */
	public void setMarcaSubPerfil(String marcaSubPerfil) {
		this.marcaSubPerfil = marcaSubPerfil;
	}
	/**
	 * @return the urlImagen
	 */
	public String getUrlImagen() {
		return urlImagen;
	}
	/**
	 * @param urlImagen the urlImagen to set
	 */
	public void setUrlImagen(String urlImagen) {
		this.urlImagen = urlImagen;
	}
	/**
	 * @return the txtPerfil
	 */
	public String getTxtPerfil() {
		return txtPerfil;
	}
	/**
	 * @param txtPerfil the txtPerfil to set
	 */
	public void setTxtPerfil(String txtPerfil) {
		this.txtPerfil = txtPerfil;
	}
	

}
